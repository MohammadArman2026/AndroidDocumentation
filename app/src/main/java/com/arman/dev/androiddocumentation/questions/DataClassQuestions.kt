/**
 * why data class cant be sealed?
 * sealed class is meant to be subclassed means there would the subclasses of it.
 * but a data class is a self contained data holder.These two intention directly
 * contradict each other.
 *
 * sealed data class Result(val status :String)
 * data class Success(val msg :String) :Result("ok")
 *
 * hypothetically compiler will generate the equals function for the Result class and when
 * it is compared with Success("no") it will return true because both have the status "OK"
 * which is dangerous.
 */


/**
 * why data class cant be abstract?
 * abstract data class is to be extended.
 * it cant be instantiated, and data class need to be instantiated.
 * some one will extend and will add some property to the data class.
 * again the same problem of equals() will arise.
 * issue with copy() , because the abstract class cant be instantiated so what will
