/**
 * what is data class?
 * A data class in Kotlin is a class specifically designed to hold data.
 * Declared with the data keyword, Kotlin automatically generates several
 * useful methods based on the properties declared in the primary constructor.
 * data from api.
 * data from database is modeled using the data class.
 * it should have at least one property.
 * it cant be open/abstract/sealed.
 *
 * compiler automatically generates some methods for it, without the data class we manually
