 * val g1 = Graph()
 * val g2 = Graph()
 * val n1 = g1.Node()
 * val n2 = g2.Node()
 *
 * n1==n1 ?? what will this result. true ? both are belonging to the different group.
 * so this will be causing the silent bug.
 *
 * inconsistency in hashcode , because both will generate the same hashcode but they belongs
