data class Charger(
    val name : String ,
    val power : Int ,
    val color: String
){
    fun copyInstance(
        name: String = this.name,
        power: Int = this.power,
        color: String = this.color
    ): Charger = Charger(
            name = name ,
            power = power ,
            color = color
        )

    fun compareEquals(other : Any?): Boolean{
        if(this === other) return true  //same reference

        if(other==null || other !is Charger) return false //null and type

        return this.name == other.name &&
                this.color == other.color &&
                this.power == other.power

    }

    override fun hashCode(): Int {
        var result = name.hashCode()
        result = result*31 + power.hashCode()
        result = result*31 +color.hashCode()
        return  result
    }

    override fun toString(): String {
        return "Charger(name = ${this.name},power = ${this.power}, color = ${this.color})"
    }

//   operator fun component1(): String = name

}


